I_AM_A_POET = """
I Am A Poet

who knows Its way past 1984
and since farms ain't popular anymore
Life is now compared to a zoo
with selectively locked doors
Sheep, Shepherds, and lions
locked in the same hell hole
Displayed in a cage of lost ways
full of lost souls
With a big sign at entrance way
that says
Don't feed the animals
They will feed themselves
No matter what you told
All some know is their role
Miss lead till their last breath
they want to be sheep
Refusing help
don't want a shepherd to lead
This ain't no feel good stories
or lies agreed upon
Pretending everybody wants peace
You're wrong
when they got a war at home
they cant let you get away
with getting along
U know the one about sheep
with wolves clothes on
As a poet
its my duty to tell you
what's really going on
We live in a land of the lower Man
Reality has a food chain
As a Poet
I Have a simple rule
Its ingrained in my brain
Lead everything I can
Either do or don't
no such thing as trying
Sometimes I'm the Poet and the Lion
I see tasty morsels of stupidity
Thrown at me
These chunks are oozing lust lies and alibis
stuff that commonly don't make sense
Some have already been
chewed on and spit out
Instead of diving in
I retract my Claws and Jaws
I swipe away idiots with my paws
Because I am a poet I
look to follow divine law,
I give the most high the praise
and ask forgiveness every hour
I am not weak and I despise cowards
This Poets knows
This is a dog eat dog world
might makes right is the power
When times are hard
I'd rather be the one eating
then the one been devoured
I walk what I talk
I feed off what's real to feel
I Don't walk around
Looking like a meal
I am not meat
I am a Poet
Our life is more than
Eat or be eaten
If I got to win or get beaten
I'm leading
I am more than a Sheep
I am a Poet
I am following the will of that
greater than me
Listening to my elders and my seeds
I transform into a shepherd
my spirit tells me to lead
This isn't just poetry this is history
I Look past disbelief and move on faith
not to see life as I wish it to be
But see it for how Life really is
To this day
countless clues have come my way
This poet is Past the point
of letting go of bad traditions
This is me hearing that divine voice
and listening
Accepting the lessons
that teach pay attention
I focus
when saving my soul is mentioned
I am a Poet
I seek Reasons my awareness
remain open to change
MY Priorities and perspectives
have been re-arranged
I seek truth In the pain
I move past lies
I Measure the equality
in eye for an eye
Crime for crime
Now both are half blind
My inner eye sees the truth as absolute
I don't see my personal development
as irrelevant
I Consider this message heaven sent
I am a Poet all the prophets were poets
who were god sent
I don't want to tell or be
told lullabies or fed delusions
I am here for solutions not more confusion
My mission is to do what's right
As a Poet I am that thief in the night
When I steal its to satisfy my soul
Don't expect or despise me
I shall repay you seven fold
What I took was mine anyway
pay it forward
Trickle down some karma
pick it up along the way
As a poet
its my duty to walk the words I say
I am a Poet
"""


